"""
Non-Modellable Risk Factor (NMRF) Stressed Expected Shortfall (SES).

NPR 2.0 profile mechanics:

    - Type A NMRFs: included in both IMCC and SES.
    - Type B NMRFs: included in SES only, with a policy-supplied partial-correlation parameter.

Aggregation formulas used here:

    Type A SES (zero correlation under the NPR 2.0 proposed formula):
        SES_A_term = sum(SES_i^2 for i in Type_A)

    Type B SES (partial correlation via rho parameter):
        SES_B_term =
            rho    * (sum(SES_i))^2
            + (1 - rho) * sum(SES_i^2)

    Combined SES:
        SES = sqrt(SES_A_term + SES_B_term)

The capital layer consumes direct, stepwise, full-revaluation, or max-loss
fallback stress artifacts produced by an upstream valuation run. It validates
those artifacts, extracts vectorized SES values, routes Type A / Type B NMRFs,
and aggregates capital. The labelled linear / sensitivity-based helper remains
an explicit approximation for tests and demos only.

Regulatory traceability:
    Basel MAR33 NMRF stress-scenario capital; U.S. NPR 2.0 SES treatment for
    Type A / Type B NMRFs; EU CRR Article 325bk. See
    docs/REGULATORY_TRACEABILITY.md.
"""

