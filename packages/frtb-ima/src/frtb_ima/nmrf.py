@dataclass(frozen=True)
class NMRFCapitalResult:
    """NMRF SES result with routing and aggregation audit detail."""

    routing: NMRFCapitalRouting
    type_a_results: tuple[NMRFStressScenarioResult, ...]
    type_b_results: tuple[NMRFStressScenarioResult, ...]
    aggregation: SESAggregationResult

    @property
    def total_ses(self) -> float:
        """Total SES scalar used by the models-based capital formula."""
        return self.aggregation.total_ses

    def as_dict(self) -> dict[str, object]:
        """Return a serialisable dictionary for reporting and audit trails."""
        return {
            "routing": self.routing.as_dict(),
            "type_a_results": [result.as_dict() for result in self.type_a_results],
            "type_b_results": [result.as_dict() for result in self.type_b_results],
            "aggregation": self.aggregation.as_dict(),
            "total_ses": self.total_ses,
        }


def _as_loss_array(
    values: Sequence[float] | npt.NDArray[np.float64],
    name: str,
) -> npt.NDArray[np.float64]:
    arr = np.asarray(values, dtype=float)
    if arr.ndim != 1:
        raise ValueError(f"{name} must be one-dimensional")
    if arr.size == 0:
        raise ValueError(f"{name} must be non-empty")
    if not np.all(np.isfinite(arr)):
        raise ValueError(f"{name} must contain only finite values")
    result = arr.astype(np.float64, copy=True)
    result.flags.writeable = False
    return result


def _as_abs_ses_array(
    values: Sequence[float] | npt.NDArray[np.float64],
    name: str,
) -> npt.NDArray[np.float64]:
    arr = np.asarray(values, dtype=float)
    if arr.ndim != 1:
        raise ValueError(f"{name} must be one-dimensional")
    if not np.all(np.isfinite(arr)):
        raise ValueError(f"{name} must contain only finite values")
    return np.abs(arr.astype(np.float64, copy=False))


def nmrf_effective_liquidity_horizon(
    risk_factor_liquidity_horizon: LiquidityHorizon,
) -> LiquidityHorizon:
    """Return the NMRF stress horizon after applying the 20-day floor."""
    if not isinstance(risk_factor_liquidity_horizon, LiquidityHorizon):
        raise TypeError("risk_factor_liquidity_horizon must be a LiquidityHorizon")
    if risk_factor_liquidity_horizon.value < _NMRF_MINIMUM_LIQUIDITY_HORIZON.value:
        return _NMRF_MINIMUM_LIQUIDITY_HORIZON
    return risk_factor_liquidity_horizon


def ses_for_nmrf_linear(sensitivity: float, shock: float) -> float:
    """
    Compute the SES contribution for a single NMRF using a linear approximation.

    SES_i = |sensitivity_i| * shock_i

    Args:
        sensitivity: First-order sensitivity (dollar P&L per unit of risk factor move).
        shock:       Stress shock size (in risk factor units, positive).

    Returns:
        SES contribution as a positive scalar.
    """
    return abs(sensitivity) * abs(shock)


def nmrf_stress_result_from_linear_sensitivity(
    risk_factor_name: str,
    sensitivity: float,
    shock: float,
    *,
    source: str = "",
    notes: str = "Linear sensitivity approximation generated by frtb-ima.",
) -> NMRFStressScenarioResult:
    """
    Build an individual NMRF SES result from the linear approximation helper.

    This is not a full regulatory direct/stepwise/full-revaluation stress
    scenario. It is retained as a transparent approximation for tests and demos.
    """
    return NMRFStressScenarioResult(
        risk_factor_name=risk_factor_name,
        method=NMRFStressMethod.LINEAR_SENSITIVITY,
        ses=ses_for_nmrf_linear(sensitivity, shock),
        generated_by_prototype=True,
        source=source,
        notes=notes,
    )


def nmrf_stress_result_from_external_ses(
    risk_factor_name: str,
    ses: float,
    method: NMRFStressMethod,
    *,
    source: str,
    notes: str = "",
) -> NMRFStressScenarioResult:
    """
    Record an externally supplied NMRF SES value and its upstream method.

    This lets the capital layer consume risk-engine direct, stepwise, or
    full-revaluation outputs without pretending this package generated them.
    """
    return NMRFStressScenarioResult(
        risk_factor_name=risk_factor_name,
        method=method,
        ses=float(ses),
        generated_by_prototype=False,
        source=source,
        notes=notes,
    )


def require_nmrf_stress_generation_supported(
    method: NMRFStressMethod,
    policy: RegulatoryPolicy | None = None,
) -> None:
    """
    Raise a named unsupported-feature error for unimplemented SES generation.

    This package can generate only the linear-sensitivity approximation. Direct,
    stepwise, and full-revaluation methods are valid upstream methods to record
    via ``nmrf_stress_result_from_external_ses`` but are not generated here.
    """
    if method == NMRFStressMethod.LINEAR_SENSITIVITY:
        return
    feature_name = _UNSUPPORTED_GENERATION_FEATURES[method]
    regime = policy.regime if policy is not None else RegulatoryRegime.FED_NPR_2_0
    raise UnsupportedRegulatoryFeature(
        regime,
        feature_name,
        "NMRF stress-scenario generation",
    )


def calculate_nmrf_ses_from_revaluation(
    artifact: NMRFStressArtifact,
    policy: RegulatoryPolicy,
    *,
    allow_linear_approximation: bool = False,
) -> NMRFStressScenarioResult:
    """
    Extract one NMRF SES value from an upstream revaluation loss vector.

    The vector is expected to be a post-valuation artifact, not a pricing model
    embedded in the capital layer. ES can be negative if every scenario is a
    gain, so SES is floored at zero. ``MAX_LOSS_FALLBACK`` artifacts use the
    maximum supplied loss instead of a tail average.
    """
    if artifact.method == NMRFStressMethod.LINEAR_SENSITIVITY and not allow_linear_approximation:
        raise ValueError(
            "LINEAR_SENSITIVITY artifacts are approximation-only and require "
            "allow_linear_approximation=True"
        )

    if artifact.method == NMRFStressMethod.MAX_LOSS_FALLBACK:
        ses = max(0.0, float(np.max(artifact.losses)))
    else:
        ses = max(
            0.0,
            expected_shortfall(
                artifact.losses,
                alpha=policy.es_confidence_level,
                estimator=policy.es_estimator,
            ),
        )
    return NMRFStressScenarioResult(
        risk_factor_name=artifact.risk_factor_name,
        method=artifact.method,
        ses=ses,
        generated_by_prototype=artifact.generated_by_prototype,
        source=artifact.source,
        notes=artifact.notes,
    )


def ses_values_from_stress_results(
    results: Sequence[NMRFStressScenarioResult],
) -> tuple[float, ...]:
    """Extract SES values from individual NMRF stress scenario results."""
    return tuple(result.ses for result in results)


def aggregate_ses_type_a(values: Sequence[float] | npt.NDArray[np.float64]) -> float:
    """
    Aggregate SES contributions for Type A NMRFs.

    The NPR 2.0 proposed SES formula aggregates Type A NMRFs with zero
    correlation, so the Type A-only component is the square root of the sum of
    squared individual SES values.
    """
    abs_vals = _as_abs_ses_array(values, "values")
    return float(math.sqrt(np.dot(abs_vals, abs_vals)))


def aggregate_ses_type_b(
    values: Sequence[float] | npt.NDArray[np.float64],
    rho: float,
) -> float:
    """
    Aggregate SES contributions for Type B NMRFs.

    Partial correlation via rho, supplied by the applicable policy:

        SES_B = sqrt(rho * (sum SES_i)^2 + (1 - rho) * sum(SES_i^2))

    Args:
        values: Individual SES contributions (positive scalars).
        rho:    Correlation parameter. 0 = fully diversified, 1 = fully correlated.
    """
    if not (0.0 <= rho <= 1.0):
        raise ValueError(f"rho must be in [0, 1], got {rho}")

    abs_vals = _as_abs_ses_array(values, "values")
    if abs_vals.size == 0:
        return 0.0
    linear_sum = float(np.sum(abs_vals))
    sum_of_squares = float(np.dot(abs_vals, abs_vals))

    return math.sqrt(rho * linear_sum**2 + (1.0 - rho) * sum_of_squares)


def aggregate_ses_breakdown(
    type_a_values: Sequence[float] | npt.NDArray[np.float64],
    type_b_values: Sequence[float] | npt.NDArray[np.float64],
    type_b_rho: float,
) -> SESAggregationResult:
    """
    Compute total SES from Type A and Type B NMRF contributions with decomposition.

    Proposed U.S. NPR 2.0 SES formula implemented here:

        SES = sqrt(
            sum(Type_A_SES_i^2)
            + rho * (sum(Type_B_SES_i))^2
            + (1 - rho) * sum(Type_B_SES_i^2)
        )
    """
    if not (0.0 <= type_b_rho <= 1.0):
        raise ValueError(f"type_b_rho must be in [0, 1], got {type_b_rho}")

    type_a = _as_abs_ses_array(type_a_values, "type_a_values")
    type_b = _as_abs_ses_array(type_b_values, "type_b_values")

    type_a_sum_of_squares = float(np.dot(type_a, type_a))
    type_b_sum_of_squares = float(np.dot(type_b, type_b))
    type_b_linear_sum = float(np.sum(type_b))
    type_b_correlated_term = (
        type_b_rho * type_b_linear_sum**2 + (1.0 - type_b_rho) * type_b_sum_of_squares
    )
    total = math.sqrt(type_a_sum_of_squares + type_b_correlated_term)

    return SESAggregationResult(
        type_a_count=int(type_a.size),
        type_b_count=int(type_b.size),
        type_a_sum_of_squares=type_a_sum_of_squares,
        type_b_correlated_term=type_b_correlated_term,
        type_b_sum_of_squares=type_b_sum_of_squares,
        type_b_linear_sum=type_b_linear_sum,
        type_b_rho=type_b_rho,
        total_ses=total,
    )


def aggregate_ses_breakdown_for_policy(
    type_a_values: Sequence[float] | npt.NDArray[np.float64],
    type_b_values: Sequence[float] | npt.NDArray[np.float64],
    policy: RegulatoryPolicy,
) -> SESAggregationResult:
    """Compute decomposed SES using policy parameters and taxonomy support gates."""
    policy.require_capital_runtime_supported()
    if policy.uses_type_a_type_b_taxonomy:
        policy.require_type_a_type_b_taxonomy()
        if (
            policy.type_a_ses_aggregation_mode
            != TypeASESAggregationMode.ZERO_CORRELATION_ROOT_SUM_SQUARES
        ):
            raise ValueError(
                "NPR 2.0 policy must use zero-correlation root-sum-square aggregation "
                "for Type A SES"
            )
        return aggregate_ses_breakdown(
            type_a_values,
            type_b_values,
            type_b_rho=policy.type_b_ses_rho,
        )
    return aggregate_ses_breakdown(
        type_a_values,
        type_b_values,
        type_b_rho=0.0,
    )


def aggregate_ses(
    type_a_values: Sequence[float] | npt.NDArray[np.float64],
    type_b_values: Sequence[float] | npt.NDArray[np.float64],
    type_b_rho: float,
) -> float:
    """Return total SES from Type A and Type B NMRF contributions."""
    return aggregate_ses_breakdown(
        type_a_values,
        type_b_values,
        type_b_rho=type_b_rho,
    ).total_ses


def aggregate_ses_for_policy(
    type_a_values: Sequence[float] | npt.NDArray[np.float64],
    type_b_values: Sequence[float] | npt.NDArray[np.float64],
    policy: RegulatoryPolicy,
) -> float:
    """
    Aggregate SES using policy parameters.

    This wrapper is only valid for policies that support the U.S. NPR
    Type A / Type B split. EU and UK profiles currently use broader NMRF
    terminology, so this wrapper raises a named unsupported-feature error.
    """
    return aggregate_ses_breakdown_for_policy(
        type_a_values,
        type_b_values,
        policy,
    ).total_ses


def _validate_classification_mapping(
    classifications: Mapping[str, ModellabilityStatus],
) -> None:
    if not classifications:
        raise ValueError("classifications must be non-empty")
    for risk_factor_name, status in classifications.items():
        if not risk_factor_name:
            raise ValueError("classification risk-factor names must be non-empty")
        if not isinstance(status, ModellabilityStatus):
            raise TypeError("classification values must be ModellabilityStatus")


def route_nmrf_classifications_for_capital(
    classifications: Mapping[str, ModellabilityStatus],
    policy: RegulatoryPolicy,
) -> NMRFCapitalRouting:
    """
    Route RFET classifications into IMCC and SES populations.

    Under the Fed NPR 2.0 profile, Type A NMRFs remain in IMCC and also
    require SES. Type B NMRFs require SES only. Under UK CRR / EU comparison
    profiles, modellable factors stay in IMCC and all NMRF statuses feed SES only.
    """
    policy.require_capital_runtime_supported()
    _validate_classification_mapping(classifications)

    modellable: list[str] = []
    type_a: list[str] = []
    type_b: list[str] = []
    for risk_factor_name, status in classifications.items():
        if status == ModellabilityStatus.MODELLABLE:
            modellable.append(risk_factor_name)
        elif status == ModellabilityStatus.TYPE_A_NMRF:
            type_a.append(risk_factor_name)
        elif status == ModellabilityStatus.TYPE_B_NMRF:
            type_b.append(risk_factor_name)

    modellable = sorted(modellable)
    type_a = sorted(type_a)
    type_b = sorted(type_b)

    if policy.uses_type_a_type_b_taxonomy:
        policy.require_type_a_type_b_taxonomy()
        imcc_risk_factors = tuple(modellable + type_a)
    else:
        imcc_risk_factors = tuple(modellable)

    return NMRFCapitalRouting(
        modellable_risk_factors=tuple(modellable),
        type_a_nmrf_risk_factors=tuple(type_a),
        type_b_nmrf_risk_factors=tuple(type_b),
        imcc_risk_factors=imcc_risk_factors,
        ses_risk_factors=tuple(type_a + type_b),
    )


def _artifact_by_risk_factor(
    artifacts: Sequence[NMRFStressArtifact],
) -> dict[str, NMRFStressArtifact]:
    result: dict[str, NMRFStressArtifact] = {}
    for artifact in artifacts:
        if artifact.risk_factor_name in result:
            raise ValueError(f"duplicate NMRF stress artifact for {artifact.risk_factor_name}")
        result[artifact.risk_factor_name] = artifact
    return result


def _validate_required_artifacts(
    routing: NMRFCapitalRouting,
    classifications: Mapping[str, ModellabilityStatus],
    artifacts_by_name: Mapping[str, NMRFStressArtifact],
    required_methods: Mapping[str, NMRFStressMethod] | None,
    required_liquidity_horizons: Mapping[str, LiquidityHorizon] | None,
) -> None:
    classification_names = set(classifications)
    unknown = [
        risk_factor_name
        for risk_factor_name in artifacts_by_name
        if risk_factor_name not in classification_names
    ]
    if unknown:
        raise ValueError(f"NMRF stress artifacts reference unknown risk factors: {unknown}")

    modellable_artifacts = [
        risk_factor_name
        for risk_factor_name in artifacts_by_name
        if classifications[risk_factor_name] == ModellabilityStatus.MODELLABLE
    ]
    if modellable_artifacts:
        raise ValueError(
            "NMRF stress artifacts were supplied for modellable risk factors: "
            f"{modellable_artifacts}"
        )

    missing = [
        risk_factor_name
        for risk_factor_name in routing.ses_risk_factors
        if risk_factor_name not in artifacts_by_name
    ]
    if missing:
        raise ValueError(f"Missing NMRF stress artifacts for: {missing}")

    if required_methods is not None:
        for risk_factor_name in routing.ses_risk_factors:
            expected_method = required_methods.get(risk_factor_name)
            if expected_method is not None:
                actual_method = artifacts_by_name[risk_factor_name].method
                if actual_method != expected_method:
                    raise ValueError(
                        f"NMRF artifact method mismatch for {risk_factor_name}: "
                        f"expected {expected_method.value}, got {actual_method.value}"
                    )

    if required_liquidity_horizons is not None:
        for risk_factor_name in routing.ses_risk_factors:
            required_lh = required_liquidity_horizons.get(risk_factor_name)
            if required_lh is not None:
                actual_lh = artifacts_by_name[risk_factor_name].liquidity_horizon
                if actual_lh.value < required_lh.value:
                    raise ValueError(
                        f"NMRF artifact liquidity horizon too short for {risk_factor_name}: "
                        f"required at least {required_lh.value}, got {actual_lh.value}"
                    )


def calculate_nmrf_capital_for_policy(
    classifications: Mapping[str, ModellabilityStatus],
    artifacts: Sequence[NMRFStressArtifact],
    policy: RegulatoryPolicy,
    *,
    required_methods: Mapping[str, NMRFStressMethod] | None = None,
    required_liquidity_horizons: Mapping[str, LiquidityHorizon] | None = None,
    allow_linear_approximation: bool = False,
    run_id: str | None = None,
    desk_id: str | None = None,
) -> NMRFCapitalResult:
    """
    Validate NMRF stress artifacts, extract SES, and aggregate Type A / Type B.

    Missing Type A or Type B stress artifacts are hard errors. This prevents the
    capital layer from silently falling back to the linear approximation helper.
    """
    policy.require_capital_runtime_supported()
    routing = route_nmrf_classifications_for_capital(classifications, policy)
    artifacts_by_name = _artifact_by_risk_factor(tuple(artifacts))
    _validate_required_artifacts(
        routing,
        classifications,
        artifacts_by_name,
        required_methods,
        required_liquidity_horizons,
    )

    type_a_results = tuple(
        calculate_nmrf_ses_from_revaluation(
            artifacts_by_name[risk_factor_name],
            policy,
            allow_linear_approximation=allow_linear_approximation,
        )
        for risk_factor_name in routing.type_a_nmrf_risk_factors
    )
    type_b_results = tuple(
        calculate_nmrf_ses_from_revaluation(
            artifacts_by_name[risk_factor_name],
            policy,
            allow_linear_approximation=allow_linear_approximation,
        )
        for risk_factor_name in routing.type_b_nmrf_risk_factors
    )
    aggregation = aggregate_ses_breakdown_for_policy(
        ses_values_from_stress_results(type_a_results),
        ses_values_from_stress_results(type_b_results),
        policy,
    )
    result = NMRFCapitalResult(
        routing=routing,
        type_a_results=type_a_results,
        type_b_results=type_b_results,
        aggregation=aggregation,
    )
    logger.info(
        "nmrf_capital_complete",
        extra=calculation_log_extra(
            run_id=run_id,
            desk_id=desk_id,
            regime=policy.regime.value,
            total_ses=result.total_ses,
            type_a_count=aggregation.type_a_count,
            type_b_count=aggregation.type_b_count,
            ses_risk_factor_count=len(routing.ses_risk_factors),
            imcc_risk_factor_count=len(routing.imcc_risk_factors),
        ),
    )
    return result
